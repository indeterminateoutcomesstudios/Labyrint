    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            int d = Integer.valueOf(jTextField1.getText());
            int scale = Integer.valueOf(jTextField2.getText());
            if ((d > 1) && (scale > 4)) {
                jPanel1.newMaze(d, scale);
                jCheckBox1.setSelected(false);
            }
            else if(d<=1){
                JOptionPane.showMessageDialog(null, "Il numero di caselle deve essere maggiore di 1");
            }
            else if(scale<=4){
                JOptionPane.showMessageDialog(null, "La dimensione di una casella deve essere maggiore di 4");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Errore: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

